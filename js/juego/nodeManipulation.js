
export function setRowStart(node, rowStart) {
    node.style.gridRowStart = rowStart;
}

export function setRowEnd(node, rowEnd) {
    node.style.gridRowEnd = rowEnd;
}

export function setColumnStart(node, columnStart) {
    node.style.gridColumnStart = columnStart;
}

export function setColumnEnd(node, columnEnd) {
    node.style.gridColumnEnd = columnEnd;
}